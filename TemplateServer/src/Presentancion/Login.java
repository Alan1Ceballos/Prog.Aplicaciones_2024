    private void cargarDatosDelUsuario() {
        ArrayList<Usuario> usuariosDeBaseDeDatos = this.ICU.listUsers();
        DefaultTableModel modelo = (DefaultTableModel) this.tblUsuarios.getModel();

        System.out.println("Usuarios Encontrados!!");

        for (Usuario usuario : usuariosDeBaseDeDatos) {
            Object[] nuevaRow = {
                usuario.getNombre(),
                usuario.getApellido(),
                usuario.getEmail(),
                usuario.getPassword(),
            };
            modelo.addRow(nuevaRow);
        }
    }
    
    private void limpiarTablaUsuarios() {
        DefaultTableModel modelo = (DefaultTableModel) this.tblUsuarios.getModel();
        modelo.setRowCount(0);
    }
    
    public void recargarDatosDelUsuario() {
        limpiarTablaUsuarios();
        cargarDatosDelUsuario();
    }

